#!/usr/bin/env node

import { existsSync, readFileSync } from "node:fs";
import { resolve } from "node:path";
import { spawnSync } from "node:child_process";
import { fileURLToPath } from "node:url";

const repoRoot = resolve(fileURLToPath(new URL("..", import.meta.url)));
const args = parseArgs(process.argv.slice(2));
const expectedBaseUrl = args["base-url"] || "https://shibei-production.up.railway.app";
const expectedBundleId = args["bundle-id"] || "com.maxhan.shibei";
const checks = [];

if (args.help) {
  printUsage();
  process.exit(0);
}

const deploymentIntent = readTextArg("deployment-intent", "deployment_intent_present");
if (deploymentIntent !== null) {
  checks.push(check(
    "deployment_intent_title",
    deploymentIntent.includes("# V2 Production Deployment Intent"),
    "deployment intent artifact must be generated by V2 Production Railway Deploy"
  ));
  checks.push(check(
    "deployment_intent_previous_deployment",
    hasFilledMarkdownField(deploymentIntent, "Previous production deployment id"),
    "deployment intent must record previous production deployment id"
  ));
  checks.push(check(
    "deployment_intent_database_backup",
    hasFilledMarkdownField(deploymentIntent, "Database backup reference"),
    "deployment intent must record database backup reference"
  ));
}

const gateEvidence = readJsonArg("gate-json", "production_gate_json_present");
if (gateEvidence) {
  assertProductionEvidence(gateEvidence, {
    prefix: "production_gate",
    expectedBaseUrl,
    expectedBundleId,
    smokeRequested: false,
    smokeStatus: "skipped"
  });
}

const smokeEvidence = readJsonArg("smoke-json", "production_smoke_json_present");
if (smokeEvidence) {
  assertProductionEvidence(smokeEvidence, {
    prefix: "production_smoke",
    expectedBaseUrl,
    expectedBundleId,
    smokeRequested: true,
    smokeStatus: "passed"
  });
}

const phoneE2E = readTextArg("phone-e2e", "phone_e2e_record_present");
if (phoneE2E !== null) {
  for (const marker of [
    "create chapter",
    "progress",
    "review",
    "source return",
    "favorites",
    "notifications"
  ]) {
    checks.push(check(
      `phone_e2e_${marker.replace(/\s+/g, "_")}`,
      phoneE2E.toLowerCase().includes(marker),
      `phone E2E record must mention ${marker}`
    ));
  }
}

if (args["signed-app"]) {
  const signing = spawnSync(process.execPath, [
    resolve(repoRoot, "tools/ios-signing-guard.mjs"),
    "--app",
    resolveFromRepo(args["signed-app"]),
    "--bundle-id",
    expectedBundleId
  ], { encoding: "utf8" });
  checks.push(check(
    "ios_signing_guard",
    signing.status === 0,
    "signed app must pass tools/ios-signing-guard.mjs"
  ));
  if (signing.status !== 0) {
    const output = `${signing.stdout || ""}${signing.stderr || ""}`.trim();
    if (output) {
      checks.push(check("ios_signing_guard_detail", false, output.split(/\r?\n/).slice(-5).join(" | ")));
    }
  }
} else {
  checks.push(check(
    "signed_app_argument_present",
    false,
    "pass --signed-app /path/to/拾贝.app after TestFlight/App Store export"
  ));
}

console.log("# Shibei V2 Production Release Evidence Guard");
console.log(`baseUrl=${expectedBaseUrl}`);
console.log(`bundleId=${expectedBundleId}`);
console.log("");
for (const item of checks) {
  console.log(`${item.ok ? "PASS" : "FAIL"} ${item.name} - ${item.detail}`);
}

const failed = checks.filter((item) => !item.ok);
if (failed.length > 0) {
  console.error("");
  console.error(`Production release evidence guard failed: ${failed.map((item) => item.name).join(", ")}`);
  process.exit(1);
}

function parseArgs(argv) {
  const parsed = {};
  for (let index = 0; index < argv.length; index += 1) {
    const item = argv[index];
    if (!item.startsWith("--")) continue;
    const key = item.slice(2);
    const next = argv[index + 1];
    if (!next || next.startsWith("--")) {
      parsed[key] = "1";
    } else {
      parsed[key] = next;
      index += 1;
    }
  }
  return parsed;
}

function printUsage() {
  console.log([
    "Usage:",
    "  npm run check:production-release-evidence -- \\",
    "    --deployment-intent docs/production-readiness-evidence/deployment-intent.md \\",
    "    --gate-json docs/production-readiness-evidence/production-gate.json \\",
    "    --smoke-json docs/production-readiness-evidence/production-smoke.json \\",
    "    --phone-e2e docs/production-readiness-evidence/phone-e2e.md \\",
    "    --signed-app /path/to/拾贝.app",
    "",
    "This guard is intentionally strict. Run it only after deploy, no-side-effect gate, smoke, phone E2E, and final signed export are complete."
  ].join("\n"));
}

function readTextArg(argName, checkName) {
  const path = args[argName];
  checks.push(check(checkName, Boolean(path), `--${argName} is required`));
  if (!path) return null;
  const absolutePath = resolveFromRepo(path);
  checks.push(check(`${checkName}_file_exists`, existsSync(absolutePath), `${absolutePath} must exist`));
  if (!existsSync(absolutePath)) return null;
  return readFileSync(absolutePath, "utf8");
}

function resolveFromRepo(path) {
  return resolve(repoRoot, path);
}

function readJsonArg(argName, checkName) {
  const text = readTextArg(argName, checkName);
  if (text === null) return null;
  try {
    return JSON.parse(text);
  } catch (error) {
    checks.push(check(`${checkName}_valid_json`, false, `must be valid JSON: ${error.message}`));
    return null;
  }
}

function assertProductionEvidence(evidence, options) {
  const { prefix, expectedBaseUrl, expectedBundleId, smokeRequested, smokeStatus } = options;
  checks.push(check(`${prefix}_status_passed`, evidence.status === "passed", "evidence.status must be passed"));
  checks.push(check(`${prefix}_base_url`, evidence.baseUrl === expectedBaseUrl, `baseUrl must be ${expectedBaseUrl}`));
  checks.push(check(`${prefix}_production_mode`, evidence.productionMode === true, "productionMode must be true"));
  checks.push(check(`${prefix}_failed_checks_empty`, Array.isArray(evidence.failedChecks) && evidence.failedChecks.length === 0, "failedChecks must be empty"));
  checks.push(check(`${prefix}_smoke_requested`, evidence.smokeRequested === smokeRequested, `smokeRequested must be ${smokeRequested}`));
  checks.push(check(`${prefix}_smoke_status`, evidence.smoke?.status === smokeStatus, `smoke.status must be ${smokeStatus}`));
  checks.push(check(`${prefix}_database_ok`, evidence.database?.ok === true, "database.ok must be true"));
  checks.push(check(`${prefix}_apns_configured`, evidence.apns?.configured === true, "apns.configured must be true"));
  checks.push(check(`${prefix}_apns_production`, evidence.apns?.environment === "production", "apns.environment must be production"));
  checks.push(check(`${prefix}_apns_bundle_id`, evidence.apns?.bundleId === expectedBundleId, `apns.bundleId must be ${expectedBundleId}`));
  for (const capability of [
    "v2ChapterGeneration",
    "v2ReviewSessions",
    "favoriteQuestions",
    "notifications",
    "sourceAnchors"
  ]) {
    checks.push(check(
      `${prefix}_capability_${capability}`,
      evidence.capabilities?.[capability] === true,
      `capabilities.${capability} must be true`
    ));
  }
}

function hasFilledMarkdownField(markdown, label) {
  const escaped = label.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
  const match = markdown.match(new RegExp(`- ${escaped}:\\s*` + "`([^`]+)`"));
  if (!match) return false;
  const value = match[1].trim();
  return value.length > 0 && !/^(n\/a|none|null|todo|unknown|pending|-+)$/i.test(value);
}

function check(name, ok, detail) {
  return { name, ok: Boolean(ok), detail };
}
